var currentLocale = 'en';

function t(key) {
  var dict = I18N[currentLocale] || I18N.en;
  return dict[key] || I18N.en[key] || key;
}

function applyLocale() {
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-html');
    el.innerHTML = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = t(key);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-title');
    el.title = t(key);
  });
  // Update all select option translations
  document.querySelectorAll('select').forEach(function(sel) {
    sel.querySelectorAll('option[data-i18n]').forEach(function(opt) {
      opt.textContent = t(opt.getAttribute('data-i18n'));
    });
  });
  // Update language switcher active state
  document.querySelectorAll('.lang-switch button').forEach(function(btn) {
    btn.classList.toggle('active', btn.dataset.lang === currentLocale);
  });
}

function setLocale(lang) {
  if (!I18N[lang]) return;
  currentLocale = lang;
  localStorage.setItem('tma1-locale', lang);
  applyLocale();
  // Re-render dynamic content in the active view/tab so translated labels regenerate.
  if (typeof refreshCurrentView === 'function') {
    refreshCurrentView();
  } else if (typeof switchView === 'function' && typeof currentView !== 'undefined' && currentView) {
    switchView(currentView, true);
  }
}

function initLocale() {
  var saved = localStorage.getItem('tma1-locale');
  if (saved && I18N[saved]) {
    currentLocale = saved;
  } else {
    // Auto-detect from browser language
    var lang = (navigator.language || '').substring(0, 2);
    if (I18N[lang]) currentLocale = lang;
  }
  applyLocale();
}
